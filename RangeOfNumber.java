 import java.lang.*;

class RangeOfNumber{
 public static void main(String args[])
{
	System.out.print("The min  range of the number is : ");
	System.out.println(Integer.MIN_VALUE);
	System.out.println("The max range of the number is : "+Integer.MAX_VALUE);
	System.out.println("The number of bytes: "+Integer.BYTES);

	System.out.print("The min  range of the number is : ");
	System.out.println(Byte.MIN_VALUE);
	System.out.println("The max range of the number is : "+ Byte.MAX_VALUE);
	System.out.println("The number of bytes : "+ Byte.BYTES);
}
}