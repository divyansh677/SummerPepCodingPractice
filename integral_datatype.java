

class integral_datatype{
	public static void main(String args[])
	{
	System.out.println("Integral_datatypes in java ");
	
	int a=23;
	char b='A';
	float f=23.4f;
	double d=34.345d;
	byte c=2;
	System.out.println("The integer is : "+a);
	System.out.println("The character is : "+b);
	System.out.println("The float is : "+ f);
	System.out.println("The Double is : "+d);
	System.out.println("The Byte is : " + c);
	
	}

}